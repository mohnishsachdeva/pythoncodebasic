# Program to demonstrate basic data types in Python

# Integer
a = 10
print("Integer value:", a, "Type:", type(a))

# Float
b = 25.5
print("Float value:", b, "Type:", type(b))

# String
c = "Hello Python"
print("String value:", c, "Type:", type(c))

# Boolean
d = True
print("Boolean value:", d, "Type:", type(d))

# Complex number
e = 3 + 4j
print("Complex value:", e, "Type:", type(e))

# List
f = [1, 2, 3, 4]
print("List value:", f, "Type:", type(f))

# Tuple
g = (10, 20, 30)
print("Tuple value:", g, "Type:", type(g))

# Set
h = {1, 2, 3}
print("Set value:", h, "Type:", type(h))

# Dictionary
i = {"name": "Mohnish", "age": 30}
print("Dictionary value:", i, "Type:", type(i))
